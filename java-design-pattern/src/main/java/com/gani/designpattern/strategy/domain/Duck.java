package com.gani.designpattern.strategy.domain;

public abstract class Duck {

    /**
     * 꽥꽥
     */
    abstract void quack();

    /**
     * 헤엄 헤엄
     */
    abstract void swim();

    /**
     * 우째 생겼습니까
     */
    abstract void display();

    /**
     * 오리는 날 수 있다!
     */
    abstract void fly();

}
